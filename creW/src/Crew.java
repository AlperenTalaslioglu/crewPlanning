public class Crew {
	private String crewName;

	// constructor
	public Crew(String crewName) {
		this.crewName = crewName;
	}

	// getter and setter
	public String getCrewName() {
		return crewName;
	}

	public void setCrewName(String crewName) {
		this.crewName = crewName;
	}

	public String toString() {
		return crewName;
	}

}
